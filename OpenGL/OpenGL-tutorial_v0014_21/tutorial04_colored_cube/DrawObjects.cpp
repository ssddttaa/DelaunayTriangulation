void DrawObjects::createCubeWithTranslation(float xOffset, float yOffset, float zOffset, float scale, GLfloat *vertex_buffer, int* totalVertices)
{
    *totalVertices += 108;
    for(int i = 0 ;i<36; i++)
    {
        vertex_buffer[i*3] = (vertex_buffer[i*3]*scale)+xOffset;
        vertex_buffer[(i*3)+1]= (vertex_buffer[(i*3)+1]*scale)+yOffset;
        vertex_buffer[(i*3)+2] = (vertex_buffer[(i*3)+2]*scale)+zOffset;
    }
    
    
}
